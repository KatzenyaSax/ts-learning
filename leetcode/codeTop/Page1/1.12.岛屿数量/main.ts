
class Queue{
    queue: [number,number][] = [];
    bottom : number = 0;
    top : number = 0;

    enqueue(tuple:[number, number]){
        this.queue[this.top++] = tuple;
    }

    dequeue():[number, number]{
        return this.queue[this.bottom++];
    }

    isEmpty():boolean{
        return this.top==this.bottom;
    }

    reset(){
        this.bottom = 0;
        this.top = 0;
    }

}

function numIslands(grid: string[][]): number {
    
    let matrix = grid;

    let num = 0;

    let queue = new Queue();

    for(let i=0; i<matrix.length; i++){
        for(let j=0; j<matrix[0].length; j++){
            if(matrix[i][j]=="1"){
                matrix[i][j] = "0";
                queue.enqueue([i,j]);
                while(!queue.isEmpty()){    //元组坐标，入队列，遍历队列染色
                
                    let tuple = queue.dequeue();
                    //console.log(tuple);
                    let x : number = tuple[0];
                    let y : number = tuple[1];
                    matrix[x][y] = "0";


                    if(x+1<matrix.length &&matrix[x+1][y]=="1"){
                        matrix[x+1][y] = "0";
                        queue.enqueue([x+1, y]);
                    } 
                    if(y+1<matrix[0].length && matrix[x][y+1]=="1"){
                        matrix[x][y+1] = "0";
                        queue.enqueue([x, y+1]);
                    } 
                    if(x-1>=0 && matrix[x-1][y]=="1"){
                        matrix[x-1][y] = "0";
                        queue.enqueue([x-1, y]);
                    } 
                    if(y-1>=0 && matrix[x][y-1]=="1"){
                        matrix[x][y-1] = "0";
                        queue.enqueue([x, y-1]);
                    } 
                    if(queue.isEmpty()) break;
                }
                queue.reset();
                num++;
            }
        }
    }
    return num;
};

